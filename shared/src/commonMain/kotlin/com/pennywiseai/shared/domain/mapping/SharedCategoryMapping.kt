    // --- helpers ---
    private fun matches(
        merchantRaw: String,
        anyOf: Set<String>,
        noneOf: Set<String> = emptySet()
    ): Boolean {
        val m = merchantRaw.lowercase()
        if (noneOf.any { m.contains(it) }) return false

        return anyOf.any { keyword ->
            if (!keyword.contains(" ")) {
                val regex = "\\b${keyword.replace(".", "\\.")}\\b".toRegex()
                regex.containsMatchIn(m)
            } else {
                m.contains(keyword)
            }
        }
    }

